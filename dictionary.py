# Define two sets
E = {0, 2, 4, 6, 8}
N = {1, 2, 3, 4, 5}

# Perform set operations
print("Union of E and N is", E | N)                   # Union
print("Intersection of E and N is", E & N)            # Intersection
print("Difference of E and N is", E - N)              # Difference (items in E not in N)
print("Symmetric difference of E and N is", E ^ N)    # Symmetric difference (items in E or N but not both)
